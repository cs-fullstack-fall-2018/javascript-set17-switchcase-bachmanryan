var number = parseInt(prompt("Enter 1,2,3,4 or 5"));

console.log("Entering switch block");
switch (number) {
    case 1:
        alert("1");
        break;

    case 2:
        alert("2");
        break;

    case 3:
        alert("3");
        break;

    case 4:
        alert("4");
        break;

    case 5:
        alert("5");
        break;
}