var number = prompt("Enter your Letter Grade: ");

console.log("Entering switch block");
switch (number) {
    case 'A':
        alert("90 to 100");
        break;

    case 'B':
        alert("80 to 89");
        break;

    case 'C':
        alert("70 to 79");
        break;

    case 'D':
        alert("60 to 69");
        break;

    case 'F':
        alert("0 to 59");
        break;
}